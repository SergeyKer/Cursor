doc.add_paragraph('• «Подключил 30% целевых профильных дилеров, включая крупнейших федералов (ТОП-100 рынка СИЗ), выстроил прозрачную модель ценообразования (РРЦ)».')
doc.add_paragraph('• «Взял на себя локализацию: оцифровал английский продукт, сертифицировал его для РФ и вывел на федеральные маркетплейсы (ВсеИнструменты, Ozon, WB). То есть я умею доводить сложный продукт от завода до цифровой полки дилера».')

