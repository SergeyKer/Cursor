import sys
import subprocess

try:
    import docx
    from docx.shared import Pt
except ImportError:
    subprocess.check_call([sys.executable, '-m', 'pip', 'install', 'python-docx'])
    import docx
    from docx.shared import Pt

# Создаем документ
doc = docx.Document()
style = doc.styles['Normal']
font = style.font
font.name = 'Arial'
font.size = Pt(12)

def add_heading(text, level=1):
    heading = doc.add_heading(text, level=level)
    for run in heading.runs:
        run.font.name = 'Arial'

add_heading('Шпаргалка: Краткий рассказ по ключевым местам работы (для интервью)', 0)

doc.add_paragraph(
    'Формат "Elevator Pitch" по каждой компании. Задача — не перечислять все обязанности, '
    'а выдать 2-3 убойных факта (цифры + суть), которые покажут вас как системного Комдира.'
)

# ----------------- Ansell -----------------
add_heading('1. Ansell (2013–2017) — Руководитель направлений продаж по РФ и СНГ', 1)
doc.add_paragraph(
    'О чем это говорит Gward: Вы знаете рынок СИЗ рук изнутри, со стороны мирового лидера, '
    'долю которого Gward сейчас забирает.'
)

add_heading('Что говорить (Кратко):', 2)
doc.add_paragraph('• «В Ansell я 4 года отвечал за продажи в индустриальном секторе (автопром, машиностроение, транспорт) по всей РФ и СНГ».')
doc.add_paragraph('• «Мой главный фокус был на управлении дистрибьюторами и товарной матрицей. Я вывел на рынок 25% нового ассортимента (около 400 SKU)».')
doc.add_paragraph('• «Выстроил бесперебойные поставки на склады дилеров (из Европы, Китая, Малайзии) и довел годовой оборот по ключевым клиентам до 3 млн Евро. Я знаю, как западные гиганты строят системы продаж, и готов применять эти стандарты в российских реалиях».')

# ----------------- Portwest -----------------
add_heading('2. Portwest (2020–2022) — Area Sales Manager (Moscow & ЦФО)', 1)
