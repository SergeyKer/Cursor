import sys
import subprocess

try:
    import docx
    from docx.shared import Pt
except ImportError:
    subprocess.check_call([sys.executable, '-m', 'pip', 'install', 'python-docx'])
    import docx
    from docx.shared import Pt

# Создаем документ
doc = docx.Document()

# Настраиваем шрифт по умолчанию
style = doc.styles['Normal']
font = style.font
font.name = 'Arial'
font.size = Pt(12)

def add_heading(text, level=1):
    heading = doc.add_heading(text, level=level)
    for run in heading.runs:
        run.font.name = 'Arial'

# Заголовок документа
add_heading('Как отвечать на вопрос об управлении P&L коммерческого блока', 0)

doc.add_paragraph(
    'P&L (от англ. Profit and Loss) — это отчёт о прибылях и убытках за период (месяц, квартал, год). '
    'В разрезе бизнеса это значит: сколько компания заработала (выручка) и сколько потратила (расходы), '
    'и какой итог — прибыль или убыток за этот период.'
)
doc.add_paragraph(
    'На интервью в роли коммерческого директора вас будут спрашивать не «что такое P&L», а '
    '«как вы управляете P&L коммерческого блока». То есть как ваши решения (цены, скидки, дилеры, '
    'дебиторка, маржа) влияют на прибыль, а не только на оборот.'
)

# ----------------- Раздел 1 -----------------
add_heading('1. Что имеют в виду под «управлением P&L коммерческого блока»', 1)
doc.add_paragraph(
    'Оборот (выручка) — сколько продали в рублях.\n'
    'P&L коммерческого блока — сколько осталось после того, что коммерция «съела» и что она '
    'ускорила или заморозила в деньгах.'
)
doc.add_paragraph(
    'То есть речь не о том, что «мы продали много», а о том:\n'
    '• как вы заработали маржу;\n'
    '• как не размыли её скидками и промо;\n'
    '• как не утонули в дебиторке и возвратах;\n'
    '• как не раздули стоимость продаж (бонусы, логистика под клиента, «особые условия»).'
)

# ----------------- Раздел 2 -----------------
add_heading('2. Из чего складывается «коммерческий» P&L', 1)

doc.add_paragraph('На уровне директора по продажам это звучит так:')

doc.add_paragraph(
    '1. Валовая маржа\n'
    'Выручка минус себестоимость (закуп/производство). Коммерция влияет косвенно через микс продаж '
    '(что продаём: маржинальные SKU или дешёвый объём), цены, скидки.'
)
doc.add_paragraph(
    '2. Коммерческие расходы (то, что вы контролируете или на что сильно влияете)\n'
    'Зарплата и мотивация отдела продаж, командировки, маркетинг/трейд у дилеров, бонусы партнёрам, '
    'участие в выставках, логистические условия отгрузки «под ключ».'
)
doc.add_paragraph(
    '3. Дебиторка и cash\n'
    'Продажа «в плюс по бумаге», но деньги не пришли — для бизнеса это риск и стоимость '
    '(кредитные линии, штрафы, простой оборотного капитала). Коммерция задаёт условия оплаты, '
    'лимиты, контроль просрочки.'
)
doc.add_paragraph(
    '4. Списания, возвраты, штрафы по контрактам\n'
    'Если продали не тому или на неверных условиях — прибыль съедается обратным потоком.'
)

# ----------------- Раздел 3 -----------------
add_heading('3. Как ваши решения влияют на прибыль (практика)', 1)

doc.add_paragraph('• Скидка «всем подряд»: Рост оборота, падение маржи. Решение: Грейды дилеров, скидка за объём и дисциплину, отдельные правила для новинок.')
doc.add_paragraph('• Низкая цена ради «удержать клиента»: Выиграли сделку, проиграли год. Решение: Минимальная маржа по сделке, пакеты, жесткие условия по оплате.')
doc.add_paragraph('• Дилер демпингует: Каннибализация, давление на всю сеть. Решение: Политика РРЦ/контроль, санкции, перераспределение квот.')
doc.add_paragraph('• Долгая дебиторка: Прибыль «на бумаге», касса страдает. Решение: Лимиты, предоплата, KPI менеджеров на сбор денег.')
doc.add_paragraph('• Промо и трейд без расчёта ROI: Рост расходов без маржи. Решение: Бюджет промо, тестирование, измерение отдачи.')
doc.add_paragraph('• Неправильный микс (гнать «дешёвый» SKU): Объём вырос, маржа вниз. Решение: ABC/XYZ по SKU и клиентам, фокус на маржинальные линейки.')

# ----------------- Раздел 4 -----------------
add_heading('4. Два подхода к ответу на интервью', 1)

add_heading('Подход 1: Для HR (Фокус на систему, контроль и KPI)', 2)
doc.add_paragraph('Задача: показать, что вы мыслите шире, чем просто «выполнить план по выручке любой ценой».')

doc.add_paragraph('Ваш ответ:')
p1 = doc.add_paragraph(
    '«Для меня управление P&L — это когда отдел продаж зарабатывает компании деньги, а не просто отгружает объемы. '
    'Я управляю прибыльностью через три инструмента:\n\n'
    '1. Система мотивации (KPI): Мои менеджеры получают бонусы не просто за выручку, а за валовую маржу и '
    'своевременный возврат дебиторки. Если менеджер дал клиенту максимальную скидку "лишь бы продать", он обрезал бонус сам себе.\n'
    '2. Коммерческая политика: Я жестко регламентирую скидки для дилеров. Скидка дается не за красивые глаза, '
    'а за соблюдение РРЦ, выполнение плана и финансовую дисциплину.\n'
    '3. Управление ассортиментом: У Gward 140+ моделей. Я фокусирую команду на продаже высокомаржинальных новинок '
    '(например, противоударных перчаток), а не на том, чтобы просто гнать фурами дешевый ХБ, который съедает '
    'складские ресурсы, но не дает чистой прибыли».'
)
p1.style = doc.styles['Quote'] if 'Quote' in doc.styles else doc.styles['Normal']

doc.add_paragraph('Почему это сработает на HR: Звучит очень здраво, системно, показывает вашу власть над менеджерами и понимание, откуда берутся деньги.')

add_heading('Подход 2: Для Топ-менеджмента (Фокус на юнит-экономику, оборотный капитал и ROI)', 2)
doc.add_paragraph('Задача: показать Ермаченко и Романенко, что вы умеете считать "стоимость денег" во времени и понимаете их главную боль (замороженные склады).')

doc.add_paragraph('Ваш ответ:')
p2 = doc.add_paragraph(
    '«Я смотрю на коммерческий P&L не только через призму "Выручка минус Себестоимость", но и через стоимость '
    'оборотного капитала и логистики.\n\n'
    'У Gward отличная фишка — отгрузка любого товара за 48 часов. Но чтобы это обеспечить, вы держите на '
    'складах колоссальные запасы (до 300 тысяч пар на позицию). Эти замороженные деньги давят на P&L компании. '
    'Как Комдир, я буду управлять P&L через балансировку сервиса и стоимости денег:\n\n'
    '1. ABC/XYZ анализ дилеров и ассортимента: Мы должны четко понимать юнит-экономику по каждому контракту. '
    'Если дилер категории "С" просит отгрузить редкую позицию за 48 часов, стоимость хранения этого запаса ради '
    'него может съедать всю маржу по сделке. Таких клиентов нужно переводить на предзаказ.\n'
    '2. Защита маржинальности в каналах: Рост B2B e-commerce (тот же Ozon) — это риск демпинга. Если дилеры '
    'начнут убивать цены друг друга в интернете, поползет вниз общая маржа Gward. Моя задача — выстроить '
    'дистрибьюторскую политика так, чтобы каналы не каннибализировали друг друга.\n'
    '3. LTV и стоимость привлечения: Я не раздуваю коммерческие расходы вслепую. Каждая инвестиция '
    '(ретро-бонусы дилерам, трейд-маркетинг, расширение команды) должна иметь четкий ROI. Мы инвестируем только '
    'в тех партнеров, где понимаем их пожизненную ценность (LTV) для компании».'
)
p2.style = doc.styles['Quote'] if 'Quote' in doc.styles else doc.styles['Normal']

doc.add_paragraph('Почему это сработает на ТОПов: Вы бьете в самое сердце их бизнес-модели. Вы говорите им: "Ребята, я знаю, что ваши забитые склады стоят вам кучу денег, и я знаю, как это оптимизировать со стороны продаж". Для собственника это музыка.')

# Сохраняем файл
doc.save('HR_PandL_Management.docx')
print('Created HR_PandL_Management.docx')
