import sys
import subprocess

try:
    from reportlab.lib.pagesizes import A4
    from reportlab.pdfbase import pdfmetrics
    from reportlab.pdfbase.ttfonts import TTFont
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
except ImportError:
    subprocess.check_call([sys.executable, '-m', 'pip', 'install', 'reportlab'])
    from reportlab.lib.pagesizes import A4
    from reportlab.pdfbase import pdfmetrics
    from reportlab.pdfbase.ttfonts import TTFont
    from reportlab.platypus import SimpleDocTemplate, Paragraph, Spacer
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    
import os

font_path_regular = 'C:\\Windows\\Fonts\\arial.ttf'
font_path_bold = 'C:\\Windows\\Fonts\\arialbd.ttf'

if not os.path.exists(font_path_regular):
    print("Шрифт Arial не найден. Использую стандартный шрифт.")
else:
    pdfmetrics.registerFont(TTFont('Arial', font_path_regular))
    pdfmetrics.registerFont(TTFont('Arial-Bold', font_path_bold))

doc = SimpleDocTemplate("GWARD_Summary.pdf", pagesize=A4, rightMargin=40, leftMargin=40, topMargin=40, bottomMargin=40)
styles = getSampleStyleSheet()

# Создаем стили с поддержкой кириллицы
font_name = 'Arial' if os.path.exists(font_path_regular) else 'Helvetica'
font_name_bold = 'Arial-Bold' if os.path.exists(font_path_bold) else 'Helvetica-Bold'

style_normal = ParagraphStyle(
    'Normal_Arial',
    parent=styles['Normal'],
    fontName=font_name,
    fontSize=11,
    leading=15,
    spaceAfter=10
)

style_bold = ParagraphStyle(
    'Bold_Arial',
    parent=styles['Normal'],
    fontName=font_name_bold,
    fontSize=11,
    leading=15,
    spaceAfter=10
)

style_heading = ParagraphStyle(
    'Heading_Arial',
    parent=styles['Heading2'],
    fontName=font_name_bold,
    fontSize=14,
    leading=18,
    spaceAfter=12,
    textColor='#000000'
)

style_quote = ParagraphStyle(
    'Quote_Arial',
    parent=styles['Normal'],
    fontName=font_name,
    fontSize=12,
    leading=16,
    leftIndent=15,
    rightIndent=15,
    spaceAfter=12,
    textColor='#003366',
    backColor='#f4f8ff',
    borderPadding=10
)

story = []

def add_h(text):
    story.append(Paragraph(text, style_heading))

def add_p(text):
    story.append(Paragraph(text, style_normal))

def add_b(text):
    story.append(Paragraph(text, style_bold))

def add_q(text):
    story.append(Paragraph(f"<b>ЦИТАТА (ваша речь):</b><br/><i>{text}</i>", style_quote))

add_h('САММАРИ И ШПАРГАЛКА ДЛЯ СОБЕСЕДОВАНИЯ В GWARD')
add_p('Краткая выжимка фактов о компании и <b>полные, нетронутые тексты вашей речи</b> для подготовки.')

add_h('1. Сводка о компании и рынке (Саммари)')
add_p('<b>Компания:</b> Gward (СИЗ рук, 140+ моделей, 3+ млн пар/мес). Выручка ~1.1-1.5 млрд руб. В 2024 году зафиксирован минус по чистой прибыли.')
add_p('<b>Лица:</b> Максим Романенко (Владелец), Александр Ермаченко (Ген. директор), Екатерина (HR в Красноярске).')
add_p('<b>Бренды:</b> Deltagrip, Docker, Fiesta. <b>Хиты:</b> Optima Zima, Extra, NON-cl, влагостойкие краги, антиударные перчатки.')
add_p('<b>Тренды:</b> Импортозамещение (забирают долю Ansell), укрупнение рынка, рост e-commerce.')
add_p('<b>Скрытая боль:</b> Старая модель экстенсивного роста ("переизбыток стока ради отгрузки за 48ч") начала съедать прибыль. Продавцы гонят оборот, забывая о марже. Дилеры демпингуют.')

add_h('2. Легенда об уходе из Blesk InCare')
add_p('Как элегантно объяснить ситуацию с 1С и логистикой:')
add_q('«Первые 3 года я успешно руководил направлением продаж Текстиля, мы с нуля выстроили отдел, внедрили Битрикс и перевыполняли планы. Руководство высоко оценило этот системный подход и поручило мне новый сложный операционный проект — возглавить логистику и сервис (Сменные ковры) перед самым зимним сезоном с параллельным внедрением новой, сырой 1С.<br/><br/>Я выстроил процессы, но в пик снегопадов вскрылась главная проблема матричной структуры компании: у меня была полная ответственность за сервис, но не было рычагов управления исполнителями (водителями из другого подразделения). Мое видение выхода из кризиса требовало жесткой централизации полномочий. Топ-менеджмент (французы) исторически был к этому не готов. Мы поняли, что наши взгляды на архитектуру управления кардинально расходятся, и разошлись обоюдно.<br/><br/>Этот проект помог мне сделать четкий вывод: моя сильная сторона и страсть — это B2B коммерция, управление P&L и дистрибуция, а не операционная логистика. Поэтому вакансия Gward для меня — это идеальный мэтч».')

add_h('3. Разбор вакансии (Речь для ТОП-менеджмента)')
add_p('Ответ на блок "Стратегия и экономика бизнеса":')
add_q('«Моя первая задача — провести ABC/XYZ анализ ассортимента и партнеров. Мы должны понять, какие из 140 моделей генерируют прибыль, а какие просто лежат мертвым грузом. Я внедрю жесткую кредитную политику для дилеров, чтобы сократить просроченную дебиторскую задолженность, и привяжу мотивацию менеджеров не только к выручке, но и к маржинальности и срокам возврата денег (P&L)».')

add_p('Ответ на блок "Управление дилерской сетью":')
add_q('«Нам нужна прозрачная коммерческая политика. Дистрибьютор должен получать скидку не за красивые глаза, а за объем выборки, соблюдение РРЦ (рекомендованной розничной цены), продвижение высокомаржинальных новинок (те же уникальные краги) и финансовую дисциплину. Я проведу аудит всех текущих партнеров, и мы безжалостно отключим тех, кто токсичен для нашей рентабельности».')

add_p('Ответ на блок "Построение команды":')
add_q('«Продажники часто гонятся за валом (выручкой), давая максимальные скидки. Я перестрою систему KPI. РОПы и менеджеры будут мотивированы от валовой маржи и процента сбора дебиторки. Я выстрою жесткую иерархию управления, чтобы я, как Коммерческий директор, занимался стратегией и первыми лицами VIP-партнеров, а РОПы — операционным управлением менеджерами».')

add_p('Ответ на блок "Автоматизация":')
add_q('«Я выведу управление в цифры. Мы настроим сквозную аналитику (ERP + CRM). Я хочу видеть юнит-экономику по каждой партии и каждому клиенту. Мы должны четко понимать стоимость логистики, хранения и обслуживания каждого контракта».')

add_h('4. Диалог с HR Екатериной')
add_p('Как использовать слабость внутреннего кандидата и ваш опыт:')
add_q('«Вы упоминали, что внутренний кандидат не справился с масштабом. Я это очень хорошо понимаю. Когда человек вырастает внутри, он часто остается супер-продавцом, но ему не хватает "насмотренности" и системного видения рынка сверху. У меня за плечами Ansell (мировой лидер СИЗ) и Portwest. Я видел, как строятся глобальные дистрибьюторские сети. Я принесу в Gward не просто умение продавать, а систему, архитектуру коммерческого блока, где каждый понимает свои KPI, а бизнес управляется на основе цифр, а не интуиции.<br/><br/>Екатерина, мне очень импонирует фокус Александра Ермаченко на R&D и уникальные разработки (влагостойкие краги, антиударные модели), а не просто на дешевый ХБ. У вас сильные партнеры (VanTrading, РПС Урал), но при ассортименте в 140+ моделей (от Optima Zima до садовой серии Fiesta), нужен системный подход. Управлять 30 менеджерами, разбросанными между Москвой и Красноярском, без жесткой CRM и привязки к марже — это путь к выгоранию команды. Я знаю, как выстроить систему, не ломая то хорошее, что уже создал предыдущий руководитель».')

add_h('5. Вопросы в конце интервью (Киллер-фичи)')
add_p('<b>Вопросы к HR Екатерине:</b>')
add_q('1. «Как вы оцениваете корпоративную культуру? Она больше про жесткую иерархию или про гибкость и инициативу?»<br/>2. «Какие 2-3 ключевые задачи будут стоять перед новым Комдиром на испытательный срок (первые 3 месяца)?»<br/>3. «Есть ли уже утвержденный бюджет на автоматизацию (CRM/ERP), или мне предстоит его защищать с нуля?»<br/>4. «Насколько текущая команда отдела продаж (30 чел) готова к изменениям и оцифровке процессов?»')

add_p('<b>Вопросы к ТОП-менеджменту (Романенко / Ермаченко):</b>')
add_q('1. «Вы пишете про цель — рост X3. Скажите, за счет чего собственник видит этот рост? За счет экспансии в новые регионы, вытеснения конкурентов в тендерах, или за счет запуска новых маржинальных продуктов?»<br/>2. «Какой сейчас средний цикл оборачиваемости складских запасов? Вы готовы к тому, что ради роста чистой прибыли и высвобождения оборотного капитала нам придется отказаться от отгрузки "любого товара за 48 часов" для неключевых партнеров?»<br/>3. «Насколько сейчас оцифрована дилерская сеть? Есть ли жесткая система контроля РРЦ, чтобы дилеры не каннибализировали продажи друг друга на маркетплейсах?»<br/>4. «Будет ли у меня карт-бланш на полное изменение системы мотивации коммерческого блока и, при необходимости, замену части команды?»')

