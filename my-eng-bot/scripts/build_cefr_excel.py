def fill_sheet(ws, headers, rows):
    ws.append(headers)
    for row in rows:
        ws.append(row)

    for cell in ws[1]:
        cell.font = Font(bold=True)

    ws.freeze_panes = "A2"

    for column in ws.columns:
        letter = column[0].column_letter
        max_len = 0
        for cell in column:
            value = "" if cell.value is None else str(cell.value)
            max_len = max(max_len, len(value))
            if cell.row > 1:
                cell.alignment = Alignment(vertical="top", wrap_text=True)
        ws.column_dimensions[letter].width = min(max(14, max_len + 2), 70)


def main():
    wb = Workbook()
    summary_ws = wb.active
    summary_ws.title = "Summary"
    rules_ws = wb.create_sheet("PromptRules")

    fill_sheet(summary_ws, SUMMARY_HEADERS, SUMMARY_ROWS)
    fill_sheet(rules_ws, RULES_HEADERS, RULES_ROWS)

    wb.save("CEFR_Levels.xlsx")
    print("CEFR_Levels.xlsx")


if __name__ == "__main__":
    main()
